package idea.ejer4;

public class SmartDevice {
    String name;
    String model;
    String manufacturer;
    String serialNumber;
    String version;


    public SmartDevice() {

    }

    public SmartDevice(String name, String model, String manufacturer, String serialNumber, String version) {
        this.name = name;
        this.model = model;
        this.manufacturer = manufacturer;
        this.serialNumber = serialNumber;
        this.version = version;


    }
}